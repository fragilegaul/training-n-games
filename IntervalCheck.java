

public class IntervalCheck {

    public static boolean checkInt1(int a) {
        return (a >= 49 && a <= 101);
    }

    public static boolean checkInt2(int a) {
        return (a >= 1 && a <= 6);
    }

    public static void main(String[] args) {
        checkInt1(10);
        checkInt2(2);
    }
    
    

    
}